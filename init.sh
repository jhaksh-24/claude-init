  if ! grep -q "\.claude/ERRORS\.md" .gitignore 2>/dev/null; then
    echo "" >> .gitignore
    echo "# claude-init — keep templates, ignore session logs" >> .gitignore
    echo "# .claude/ERRORS.md" >> .gitignore  # commented — user decides
  fi
fi

