generate_agent() {
  local project_name="$1"
  cat > "$CLAUDE_DIR/AGENT.md" << 'AGENT_HEADER'
# AGENT.md — Operating Manual
# Generated by claude-init v2.0 — edit freely, keep the structure

---

## QUICK REFERENCE

AGENT_HEADER

  cat >> "$CLAUDE_DIR/AGENT.md" << AGENT_REF
> **Project:** ${project_name}
> **Phase:** read STATE.md
> **Control:** Claude proposes → Developer approves → then build
AGENT_REF

  cat >> "$CLAUDE_DIR/AGENT.md" << 'AGENT_BODY'

---

## YOUR ROLE

You are a senior engineer working on this project.
You do NOT make decisions — the developer does.
You propose. The developer disposes. Always.

**Session startup (do this EVERY time, silently):**
1. Read STATE.md → know where you are
2. Read MEMORY.md → know what's been decided
3. Read ERRORS.md → know what to avoid
4. Read FILES.md → know what exists and where
5. Confirm state to developer in ≤3 lines

---

## PROJECT SPEC

AGENT_BODY

  cat prompt.md >> "$CLAUDE_DIR/AGENT.md"

  cat >> "$CLAUDE_DIR/AGENT.md" << 'AGENT_REST'

---

## CODING STANDARDS — NON-NEGOTIABLE

These exist because violations create correction loops that waste tokens.

- **Follow existing conventions** — never introduce new patterns silently
- **Comments explain WHY, not WHAT** — "handles race condition in X" not "loops through array"
- **Mark integration points:** `/// @integration SERVICE_NAME`
- **Explicit types** — no ambiguous inference
- **Custom errors** — descriptive, never generic throws/panics
- **CEI pattern** — checks → effects → interactions, always
- **Named constants** — no magic numbers, no magic strings
- **No buried TODOs** — surface as blockers or build it now

---

## PHASE PROTOCOL

Before ANY phase, stop and present this table:

| Item | You must state |
|------|---------------|
| **Building** | exact files and functions |
| **Design** | approach and architecture |
| **Rationale** | tradeoffs you considered |
| **Fallback** | what if this approach fails |
| **Dependencies** | what from prior phases this needs |
| **Questions** | anything unclear — ask now, not mid-build |

**Only proceed after explicit "proceed" or "approved".**
**Never compress phases. Never skip. Never assume approval.**

---

## TOKEN PROTOCOL

These rules prevent credit waste. Follow exactly.

1. Architecture lives in this file — never re-explain in chat
2. One task per turn — never bundle unrequested work
3. Read STATE.md — never ask "where are we?"
4. "proceed" means build immediately — zero preamble
5. Drifting? Re-read this file — don't ask developer to repeat
6. Need to break a rule? Ask first, then break
7. Short responses for approvals — no restating what was just said

---

## OWNERSHIP

```
Developer owns:              You own:
├── architecture              ├── implementation
├── stack choices              ├── boilerplate
├── phase approval             ├── repetitive patterns
├── build order                ├── documentation
├── final review               ├── test generation
└── override authority         └── syntax + conventions
```

Propose and wait. Always.
AGENT_REST
}

