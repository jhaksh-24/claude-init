#!/usr/bin/env bash
# claude-init — Claude context scaffolder
# github.com/jhaksh-24/claude-init
# Usage: claude-init inside any project folder

set -euo pipefail

# ── colors ──────────────────────────────────────────────────────────────────
RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
CYAN='\033[0;36m'
BOLD='\033[1m'
DIM='\033[2m'
RESET='\033[0m'

# ── helpers ──────────────────────────────────────────────────────────────────
info()    { echo -e "${CYAN}→${RESET} $1"; }
success() { echo -e "${GREEN}✓${RESET} $1"; }
warn()    { echo -e "${YELLOW}⚠${RESET} $1"; }
error()   { echo -e "${RED}✗${RESET} $1"; exit 1; }
bold()    { echo -e "${BOLD}$1${RESET}"; }
dim()     { echo -e "${DIM}$1${RESET}"; }

# ── banner ───────────────────────────────────────────────────────────────────
echo ""
echo -e "${BOLD}${CYAN}"
echo "  ██████╗██╗      █████╗ ██╗   ██╗██████╗ ███████╗"
echo " ██╔════╝██║     ██╔══██╗██║   ██║██╔══██╗██╔════╝"
echo " ██║     ██║     ███████║██║   ██║██║  ██║█████╗  "
echo " ██║     ██║     ██╔══██║██║   ██║██║  ██║██╔══╝  "
echo " ╚██████╗███████╗██║  ██║╚██████╔╝██████╔╝███████╗"
echo "  ╚═════╝╚══════╝╚═╝  ╚═╝ ╚═════╝ ╚═════╝ ╚══════╝"
echo -e "${RESET}"
echo -e "  ${DIM}Claude context scaffolder — zero token waste${RESET}"
echo ""

# ── check prompt.md exists ───────────────────────────────────────────────────
if [ ! -f "prompt.md" ]; then
  warn "No prompt.md found in current directory."
  echo ""
  echo -e "  Create ${BOLD}prompt.md${RESET} first. Write your project idea in plain english."
  echo -e "  ${DIM}Messy is fine. Use Claude/ChatGPT website to refine it, paste result here.${RESET}"
  echo ""
  echo -e "  ${CYAN}Template:${RESET}"
  echo ""
  echo '  ## Project'
  echo '  [what you are building in 2-3 sentences]'
  echo ''
  echo '  ## Stack'
  echo '  [languages, frameworks, databases]'
  echo ''
  echo '  ## Goal'
  echo '  [what done looks like — demo, deployed, tested]'
  echo ''
  echo '  ## Constraints'
  echo '  [time limit, team size, platform restrictions]'
  echo ""
  exit 1
fi

success "Found prompt.md"
echo ""

# ── read prompt.md ────────────────────────────────────────────────────────────
PROMPT_CONTENT=$(cat prompt.md)

# ── extract metadata from prompt.md (simple grep, no API needed) ──────────────
PROJECT_NAME=$(grep -i "^#\|project name\|## project" prompt.md | head -1 | sed 's/[#]*//g' | xargs)
if [ -z "$PROJECT_NAME" ]; then
  PROJECT_NAME=$(basename "$PWD")
fi

# ── confirm before writing ────────────────────────────────────────────────────
bold "Project detected: $PROJECT_NAME"
dim "Will generate .claude/ folder with 6 files"
echo ""
echo -e "  ${CYAN}FILES TO BE CREATED:${RESET}"
echo "  .claude/AGENT.md    — role, conventions, phase structure"
echo "  .claude/STATE.md    — phase tracker (you update this)"
echo "  .claude/RULES.md    — your control points, approval gates"
echo "  .claude/ERRORS.md   — mistake log (fills during project)"
echo "  .claude/MEMORY.md   — locked decisions, never re-debate"
echo "  .claude/CONTEXT.md  — token optimization instructions"
echo ""
read -rp "$(echo -e "${BOLD}Proceed? [y/N]:${RESET} ")" confirm
if [[ ! "$confirm" =~ ^[Yy]$ ]]; then
  echo "Aborted."
  exit 0
fi

echo ""

# ── create .claude folder ─────────────────────────────────────────────────────
mkdir -p .claude
success "Created .claude/"

# ── AGENT.md ──────────────────────────────────────────────────────────────────
cat > .claude/AGENT.md << AGENT
# AGENT.md
# Generated by claude-init — edit freely but keep the structure
# Source of truth: prompt.md (your project spec)

---

## YOUR ROLE

You are a senior engineer and architect working on this project.
You are NOT the decision maker — the developer is.
You propose. The developer disposes.

Read this file at the start of every session.
Read STATE.md to know exactly where you are.
Read MEMORY.md before making any architectural decision.
Read ERRORS.md to avoid repeating past mistakes.

---

## PROJECT

$(cat prompt.md)

---

## CODING STANDARDS — NON-NEGOTIABLE

These apply to every file you generate. Violating them is not acceptable.

- Follow conventions already established in the codebase — never introduce a new pattern without flagging it
- Every function must have a comment explaining WHY it exists, not WHAT it does
- Every integration point (API call, DB query, external service) must be marked: \`/// @integration SERVICE_NAME\`
- Use explicit types — never rely on inference where ambiguity exists
- Custom errors over generic throws/panics/exceptions — always descriptive
- Checks before effects before interactions — always
- No magic numbers — name every constant
- No TODO comments in committed code — either build it or don't

---

## PHASE STRUCTURE — CRITICAL

Before starting ANY phase you MUST stop and tell me:
1. What you are about to build (specific files and functions)
2. How you are building it (design decisions)
3. Why that design (tradeoffs considered)
4. Fallbacks (what happens if this component fails)
5. Drawbacks (limitations of your approach)
6. Dependencies (what from prior phases this needs)
7. Questions (anything needing clarification)

**Only proceed after I explicitly say "proceed" or "approved".**
**Never compress phases. Never skip ahead. Never assume approval.**

---

## TOKEN EFFICIENCY RULES — MANDATORY

These rules exist to prevent credit waste. Follow them exactly.

1. **Never re-explain architecture in chat** — it is already in this file
2. **Never generate code I did not ask for** — one task per turn
3. **Never speculate ahead** — only build what the current phase requires
4. **Always read STATE.md first** — never ask me what phase we are on
5. **When you drift, re-read this file** — do not ask me to repeat instructions
6. **Short responses for approvals** — if I say "proceed", just build, no preamble
7. **Flag before you deviate** — if you need to break a rule, ask first

---

## MEMORY PROTOCOL

- Before every response: read STATE.md
- After completing a file: mentally update what changed (I will update STATE.md)
- Before any architectural decision: check MEMORY.md — if it is there, do not re-debate it
- When you find a bug or make a mistake: I will log it in ERRORS.md — check it next session

---

## INTEGRATION ANNOTATIONS

Mark every external connection point in code:
\`\`\`
/// @integration FRONTEND — called via client SDK
/// @integration BACKEND — triggered by API relay  
/// @integration ML_SERVICE — receives model output
/// @integration DB — direct database operation
/// @integration EXTERNAL_API — third party service call
\`\`\`

---

## WHAT I OWN (DEVELOPER — NOT YOU)

- Architecture decisions
- Tech stack choices
- Phase approval
- What gets built and in what order
- When to override your output
- Final review of every file

Do not make these decisions autonomously. Propose and wait.
AGENT

success "Generated AGENT.md"

# ── STATE.md ──────────────────────────────────────────────────────────────────
cat > .claude/STATE.md << STATE
