# ── CONTEXT.md ───────────────────────────────────────────────────────────────
cat > .claude/CONTEXT.md << CONTEXT
# CONTEXT.md
# Token optimization protocol — read this to understand how to work efficiently.
# This file exists because context = credits. Every wasted token is real money.

---

## WHY THIS EXISTS

Claude processes the ENTIRE conversation history on every single turn.
A 50-turn chat at 2000 tokens/turn = 100,000 tokens just in context on turn 50.
This file system keeps context FLAT instead of ever-growing.

---

## THE EFFICIENCY HIERARCHY

\`\`\`
Most expensive  →  re-explaining architecture in chat
                →  correction loops from drift
                →  speculative generation
                →  long approval messages
Least expensive →  "proceed"
                →  file reads
                →  short targeted questions
\`\`\`

---

## RULES FOR THE DEVELOPER

1. Keep your messages SHORT — "proceed", "fix X in Y", "approved"
2. NEVER re-explain the stack or architecture in chat — it is in AGENT.md
3. Update STATE.md yourself between phases — do not ask Claude to do it in chat
4. Start new conversations for new phases when context gets heavy
5. If Claude drifts, say "re-read AGENT.md" — not a long correction

---

## RULES FOR CLAUDE

1. Read AGENT.md, STATE.md, MEMORY.md, ERRORS.md at session start
2. Never ask something already answered in these files
3. One task per turn — do not bundle unrequested work
4. Phase briefing = stop and wait — never auto-proceed
5. If context window feels heavy, say so — developer will start a fresh session

---

## SESSION HANDOFF PROTOCOL

At the end of every session:
1. Developer updates STATE.md with what was completed
2. Developer logs any new decisions in MEMORY.md
3. Developer logs any mistakes in ERRORS.md
4. Next session starts with: "read .claude/ and tell me current state"
5. Claude confirms state from files — zero re-explanation needed

---

## THE ONE RULE

**Everything that can live in a file, lives in a file.**
**The chat is only for decisions and approvals.**
CONTEXT

success "Generated CONTEXT.md"

# ── .gitignore entry ──────────────────────────────────────────────────────────
