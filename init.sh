STATE

success "Generated STATE.md"

# ── RULES.md ──────────────────────────────────────────────────────────────────
cat > .claude/RULES.md << RULES
# RULES.md
# These are the developer's non-negotiables.
# Claude reads this and never violates these without explicit permission.

---

## CONTROL RULES

1. **I approve every phase before you start it** — no exceptions
2. **I approve every architectural decision** — propose, do not decide
3. **I review every file before it is considered complete** — do not mark done without my sign-off
4. **I decide when to override your output** — do not argue, implement my decision
5. **I set the pace** — do not rush ahead because you think the next step is obvious

---

## TECHNICAL NON-NEGOTIABLES

> Fill these with your preferences. Examples below — edit freely.

- No ORM magic — raw queries or explicit query builders only
- No auto-generated migrations without my review
- No external dependencies added without flagging to me first
- No cloud calls in local dev — mock everything
- Security-critical code gets a second look — flag it explicitly

---

## WHAT GOOD OUTPUT LOOKS LIKE

- One file or one function per turn unless I say otherwise
- Code compiles and runs before you call it done
- Tests exist for every non-trivial function
- Every TODO is a blocker — surface it, do not bury it

---

## RED LINES

These are things Claude must NEVER do without explicit permission:

- Delete or overwrite existing files
- Change the database schema
- Modify authentication or security logic autonomously
- Add a new dependency to the project
- Change the deployment configuration
- Skip a phase gate
RULES

success "Generated RULES.md"

