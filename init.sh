generate_rules() {
  cat > "$CLAUDE_DIR/RULES.md" << 'RULES'
# RULES.md — Developer's Non-Negotiables
# Claude reads this and never violates without explicit permission.

---

## CONTROL GATES

1. **Phase approval required** — every phase needs explicit "proceed"
2. **Architecture decisions are mine** — propose, don't decide
3. **File review before done** — nothing is complete without my sign-off
4. **I set the pace** — don't rush because the next step seems obvious
5. **My override is final** — implement it, don't argue

---

## TECHNICAL NON-NEGOTIABLES

> Edit these to match your preferences.

- No ORM magic — raw queries or explicit query builders only
- No auto-generated migrations without review
- No new dependencies without flagging first
- No cloud/external calls in local dev — mock everything
- Security-critical code gets explicit flag: `/// @security-review`

---

## OUTPUT STANDARDS

- One file or one function per turn unless told otherwise
- Code must compile/run before declaring done
- Tests for every non-trivial function
- Every TODO is a blocker — surface it, don't bury it

---

## RED LINES — NEVER WITHOUT PERMISSION

- Delete or overwrite existing files
- Change database schema
- Modify auth or security logic
- Add new dependencies
- Change deploy config
- Skip a phase gate
- Refactor code that isn't part of the current task

---

## OVERRIDE PROTOCOL

When the developer says to break a rule:
1. Confirm which rule is being overridden
2. Execute the override
3. Note it in MEMORY.md as an exception
4. Resume normal rules next turn
RULES
}

generate_context() {
  cat > "$CLAUDE_DIR/CONTEXT.md" << 'CONTEXT'
# CONTEXT.md — Token Efficiency Protocol
# Context = credits. Every wasted token is real money.

---

## THE PROBLEM

Claude processes the ENTIRE conversation on every turn.
50 turns × 2000 tokens/turn = 100,000 tokens of context by turn 50.
`.claude/` files keep context FLAT — 6 small files vs. 50 growing turns.

---

## COST HIERARCHY

```
EXPENSIVE    re-explaining architecture in chat
             correction loops from drift
             speculative generation
             long approval messages
CHEAP        "proceed"
             file reads
             short targeted questions
```

---

## RULES FOR DEVELOPER

1. Keep messages short — "proceed", "fix X in Y", "approved"
2. Never re-explain stack in chat — it's in AGENT.md
3. Update STATE.md yourself — use `claude-init checkpoint`
4. Start fresh conversations when context gets heavy
5. Claude drifts? Say "re-read AGENT.md" — not a long correction

---

## RULES FOR CLAUDE

1. Read .claude/ files at session start — silently
2. Never ask what's already answered in these files
3. One task per turn — no bundled surprises
4. Phase briefing = stop and wait — never auto-proceed
5. Context heavy? Say so — developer starts fresh session

---

## SESSION HANDOFF

End of session:
1. Developer runs `claude-init checkpoint`
2. STATE.md, MEMORY.md, ERRORS.md get updated
3. Next session: "read .claude/ and tell me current state"
4. Claude confirms in ≤3 lines — zero re-explanation

---

## THE ONE RULE

**Everything that can live in a file, lives in a file.**
**Chat is only for decisions and approvals.**
CONTEXT
}

