cmd_init() {
  local minimal="$1"
  local force="$2"

  show_banner

  # ── check prompt.md ──
  if [ ! -f "prompt.md" ]; then
    warn "No prompt.md found in current directory."
    echo ""
    echo -e "  Create ${BOLD}prompt.md${RESET} first. Describe your project — messy is fine."
    echo -e "  ${DIM}Use Claude.ai or ChatGPT to refine it, paste result here.${RESET}"
    echo ""
    echo -e "  ${CYAN}Quick template:${RESET}"
    echo ""
    echo "  ## Project"
    echo "  [what you're building]"
    echo ""
    echo "  ## Stack"
    echo "  [languages, frameworks, databases]"
    echo ""
    echo "  ## Goal"
    echo "  [what done looks like]"
    echo ""
    exit 1
  fi

  success "Found prompt.md"
  echo ""

  # ── extract project name ──
  local project_name
  project_name=$(grep -i "^#\|project name\|## project" prompt.md | head -1 | sed 's/[#]*//g' | xargs)
  if [ -z "$project_name" ]; then
    project_name=$(basename "$PWD")
  fi

  # ── check existing .claude/ ──
  if [ -d "$CLAUDE_DIR" ] && [ "$force" = "false" ]; then
    warn ".claude/ already exists."
    echo ""
    read -rp "$(echo -e "  ${BOLD}Overwrite? [y/N]:${RESET} ")" overwrite
    if [[ ! "$overwrite" =~ ^[Yy]$ ]]; then
      echo "  Aborted. Use --force to skip this prompt."
      exit 0
    fi
    echo ""
  fi

  # ── confirm ──
  bold "Project: $project_name"
  if [ "$minimal" = "true" ]; then
    dim "Mode: minimal (4 files)"
  else
    dim "Mode: full (7 files)"
  fi
  echo ""

  local file_list
  if [ "$minimal" = "true" ]; then
    file_list="AGENT.md STATE.md MEMORY.md CONTEXT.md"
  else
    file_list="AGENT.md STATE.md RULES.md MEMORY.md ERRORS.md FILES.md CONTEXT.md"
  fi

  echo -e "  ${CYAN}FILES:${RESET}"
  for f in $file_list; do
    echo "  .claude/$f"
  done
  echo ""

  read -rp "$(echo -e "${BOLD}Proceed? [y/N]:${RESET} ")" confirm
  if [[ ! "$confirm" =~ ^[Yy]$ ]]; then
    echo "Aborted."
    exit 0
  fi
  echo ""

  # ── generate ──
  mkdir -p "$CLAUDE_DIR"
  success "Created .claude/"

  generate_agent "$project_name"
  success "Generated AGENT.md"

  generate_state
  success "Generated STATE.md"

  generate_memory
  success "Generated MEMORY.md"

  generate_context
  success "Generated CONTEXT.md"

  if [ "$minimal" = "false" ]; then
    generate_rules
    success "Generated RULES.md"

    generate_errors
    success "Generated ERRORS.md"

    generate_files
    success "Generated FILES.md"
  fi

  # ── .gitignore ──
  if [ -f ".gitignore" ]; then
    if ! grep -q "\.claude/" .gitignore 2>/dev/null; then
      echo "" >> .gitignore
      echo "# claude-init — uncomment to exclude session-specific files" >> .gitignore
      echo "# .claude/ERRORS.md" >> .gitignore
      echo "# .claude/STATE.md" >> .gitignore
    fi
  fi

  # ── done ──
  echo ""
  bold "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
  success "claude-init complete"
  bold "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
  echo ""
  echo -e "  ${CYAN}NEXT:${RESET} Open Claude and send this:"
  echo ""
  echo -e "  ${BOLD}\"Read everything in .claude/ and give me your Phase 1 briefing.\"${RESET}"
  echo ""
  echo -e "  ${CYAN}LATER:${RESET} End each session with:"
  echo ""
  echo -e "  ${BOLD}claude-init checkpoint${RESET}"
  echo ""
  dim "  github.com/jhaksh-24/claude-init"
  echo ""
}

cmd_checkpoint() {
  if [ ! -d "$CLAUDE_DIR" ]; then
    error "No .claude/ directory found. Run 'claude-init' first."
  fi

  show_banner_mini
  bold "━━━ Session Checkpoint ━━━"
  echo ""

  # ── Phase update ──
  local current_phase
  current_phase=$(grep "^Phase:" "$CLAUDE_DIR/STATE.md" 2>/dev/null | head -1 || echo "unknown")
  info "Current → $current_phase"
  echo ""
  read -rp "$(echo -e "${BOLD}New phase status (or Enter to keep):${RESET} ")" new_phase

  # ── What was completed ──
  echo ""
  read -rp "$(echo -e "${BOLD}What was completed this session?${RESET} ")" completed

  # ── Files changed ──
  local files_changed=""
  local files_rows=""
  echo ""
  read -rp "$(echo -e "${BOLD}Any files created or changed this session? [y/N]:${RESET} ")" has_files
  if [[ "$has_files" =~ ^[Yy]$ ]] && [ -f "$CLAUDE_DIR/FILES.md" ]; then
    echo ""
    echo -e "  ${DIM}Enter each file one at a time. Empty path = done.${RESET}"
    echo -e "  ${DIM}Status: 1=stable  2=wip  3=broken  4=planned${RESET}"
    echo ""
    local file_names_list=""
    while true; do
      read -rp "  $(echo -e "${CYAN}File path${RESET} (or Enter to finish): ")" fpath
      [ -z "$fpath" ] && break
      read -rp "  $(echo -e "${CYAN}Purpose${RESET}: ")" fpurpose
      read -rp "  $(echo -e "${CYAN}Status${RESET} [1-4, default=2]: ")" fstatus_num
      case "${fstatus_num:-2}" in
        1) fstatus="🟢 stable" ;;
        3) fstatus="🔴 broken" ;;
        4) fstatus="⬜ planned" ;;
        *) fstatus="🟡 wip" ;;
      esac
      files_rows+="| ${fpath} | ${fpurpose} | ${fstatus} |\n"
      file_names_list+="${fpath}, "
      echo -e "  ${GREEN}✓${RESET} Logged"
      echo ""
    done
    files_changed="${file_names_list%, }"
  fi

  # ── Decisions ──
  echo ""
  read -rp "$(echo -e "${BOLD}Lock in a decision? [y/N]:${RESET} ")" has_decision
  local decision_block=""
  if [[ "$has_decision" =~ ^[Yy]$ ]]; then
    read -rp "  Decision title: " dtitle
    read -rp "  What was decided: " dwhat
    read -rp "  Why: " dwhy
    decision_block=$(printf "\n### %s\n**Date:** %s\n**Decision:** %s\n**Reason:** %s\n" "$dtitle" "$(today)" "$dwhat" "$dwhy")
  fi

  # ── Errors ──
  echo ""
  read -rp "$(echo -e "${BOLD}Log a bug or mistake? [y/N]:${RESET} ")" has_error
  local error_block=""
  if [[ "$has_error" =~ ^[Yy]$ ]]; then
    read -rp "  Error title: " etitle
    read -rp "  What happened: " ewhat
    read -rp "  Root cause: " ecause
    read -rp "  Fix applied: " efix
    error_block=$(printf "\n### %s\n**Date:** %s\n**What happened:** %s\n**Root cause:** %s\n**Fix applied:** %s\n" "$etitle" "$(today)" "$ewhat" "$ecause" "$efix")
  fi

  # ── Next session ──
  echo ""
  read -rp "$(echo -e "${BOLD}What should next session start with?${RESET} ")" next_task

  # ── Apply updates ──
  echo ""
  info "Saving checkpoint..."

  # Update STATE.md — phase line
  if [ -n "$new_phase" ]; then
    sed_inplace "s/^Phase: .*/Phase: $new_phase/" "$CLAUDE_DIR/STATE.md"
  fi

  # Update STATE.md — metadata
  sed_inplace "s/^Last Updated: .*/Last Updated: $(today)/" "$CLAUDE_DIR/STATE.md"

  # Increment session count
  local count
  count=$(grep "^Session Count:" "$CLAUDE_DIR/STATE.md" | grep -o '[0-9]*' || echo "0")
  local new_count=$((count + 1))
  sed_inplace "s/^Session Count: .*/Session Count: $new_count/" "$CLAUDE_DIR/STATE.md"

  # Update next task
  if [ -n "$next_task" ]; then
    sed_inplace "s/^Next Task: .*/Next Task: $next_task/" "$CLAUDE_DIR/STATE.md"
  fi

  # Update last completed
  if [ -n "$completed" ]; then
    sed_inplace "s/^Last Completed: .*/Last Completed: $completed/" "$CLAUDE_DIR/STATE.md"
  fi

  # Append session log entry
  local log_entry
  log_entry=$(printf "\n### Session %d — %s\n**Completed:** %s\n**Files:** %s\n**Next:** %s\n" \
    "$new_count" "$(today)" "${completed:-nothing}" "${files_changed:-none}" "${next_task:-TBD}")
  echo "$log_entry" >> "$CLAUDE_DIR/STATE.md"
  success "Updated STATE.md"

  # Append decision to MEMORY.md
  if [ -n "$decision_block" ]; then
    echo "$decision_block" >> "$CLAUDE_DIR/MEMORY.md"
    success "Added decision to MEMORY.md"
  fi

  # Append error to ERRORS.md
  if [ -n "$error_block" ]; then
    echo "$error_block" >> "$CLAUDE_DIR/ERRORS.md"
    success "Added error to ERRORS.md"
  fi

  # Write file rows to FILES.md
  if [ -n "$files_rows" ] && [ -f "$CLAUDE_DIR/FILES.md" ]; then
    local files_md="$CLAUDE_DIR/FILES.md"
    # If the table header already exists, insert rows right after the | --- | line
    if grep -q "^| File |" "$files_md" 2>/dev/null; then
      # Find the separator line and append rows after the last table row
      # We do this by appending; markdown tables accept new rows at the end
      printf "%b" "$files_rows" >> "$files_md"
    else
      # No table yet — create header + rows
      printf "\n| File | Purpose | Status |\n|------|---------|--------|\n" >> "$files_md"
      printf "%b" "$files_rows" >> "$files_md"
    fi
    # Timestamped audit trail in RECENT CHANGES
    printf "\n**%s:** %s\n" "$(today)" "$files_changed" >> "$files_md"
    success "Updated FILES.md"
  fi

  echo ""
  bold "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
  success "Checkpoint saved — session $new_count"
  bold "━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━━"
  echo ""
  dim "  Next session: \"read .claude/ and tell me current state\""
  echo ""
}

cmd_status() {
  if [ ! -d "$CLAUDE_DIR" ]; then
    error "No .claude/ directory found. Run 'claude-init' first."
  fi

  show_banner_mini

  if [ -f "$CLAUDE_DIR/STATE.md" ]; then
    echo -e "  ${CYAN}STATE${RESET}"
    grep -E "^(Phase:|Last Completed:|Next Task:|Blockers:|Last Updated:|Session Count:|Context Health:)" "$CLAUDE_DIR/STATE.md" 2>/dev/null | while IFS= read -r line; do
      echo -e "  ${DIM}│${RESET} $line"
    done
    echo ""
  fi

  if [ -f "$CLAUDE_DIR/ERRORS.md" ]; then
    local bug_count
    bug_count=$(grep -c "^### " "$CLAUDE_DIR/ERRORS.md" 2>/dev/null | tr -d '\r\n' || echo "0")
    bug_count=${bug_count:-0}
    if [ "$bug_count" -gt 0 ] 2>/dev/null; then
      echo -e "  ${YELLOW}ERRORS${RESET}  $bug_count logged"
    fi
  fi

  if [ -f "$CLAUDE_DIR/MEMORY.md" ]; then
    local decision_count
    decision_count=$(grep -c "^### " "$CLAUDE_DIR/MEMORY.md" 2>/dev/null | tr -d '\r\n' || echo "0")
    decision_count=${decision_count:-0}
    if [ "$decision_count" -gt 0 ] 2>/dev/null; then
      echo -e "  ${GREEN}DECISIONS${RESET}  $decision_count locked"
    fi
  fi

  echo ""
}

